// runNodeModulesResolutionCmd runs the node-modules missing/unused command against the main-app
// fixture, following the shared-lib workspace package, in the requested resolution mode.
func runNodeModulesResolutionCmd(t *testing.T, listUnused, listMissing, nearestPackage bool) string {
	t.Helper()
	cwd, err := testutil.FixturePath("nodeModulesResolution", "apps", "main-app")
	if err != nil {
		t.Fatalf("FixturePath: %v", err)
	}
	result, _ := NodeModulesCmd(
		cwd,
		false,                                           // ignoreType
		[]string{},                                      // entryPoints (auto-detect)
		false,                                           // countFlag
		listUnused,                                      // listUnused
		listMissing,                                     // listMissing
		false, false, false, false, false, false, false, // grouping flags
		nil, nil, nil, // pkgJsonFieldsWithBinaries, filesWithBinaries, filesWithModules
		nil, nil, // modulesToInclude, modulesToExclude
		"", "", // packageJson, tsconfigJson
		nil, // conditionNames
		FollowMonorepoPackagesValue{FollowAll: true}, // follow shared-lib
		nearestPackage,
		false, // includeDevDepsFromRoot
	)
	return result
}

// TestGetMissingNodeModules_ExtraAllowedModules verifies that includeDevDepsFromRoot's extra
// allowed modules suppress "missing" reports in both resolution modes, without otherwise changing
// detection.
func TestGetMissingNodeModules_ExtraAllowedModules(t *testing.T) {
	tree := MinimalDependencyTree{
		"/repo/packages/app/index.ts": {
			{ID: "jest", Request: "jest", ResolvedType: NotResolvedModule},
			{ID: "lodash", Request: "lodash", ResolvedType: NotResolvedModule},
		},
	}

	hasModule := func(results []MissingNodeModuleResult, name string) bool {
		for _, r := range results {
			if r.ModuleName == name {
				return true
			}
		}
		return false
	}

	for _, nearest := range []bool{false, true} {
		mode := "entry-package"
		if nearest {
			mode = "nearest-package"
		}

		t.Run(mode+": without extra allowed, both are missing", func(t *testing.T) {
			res := GetMissingNodeModulesFromTree(tree, nil, nil, map[string]bool{}, nil, nearest)
			if !hasModule(res, "jest") || !hasModule(res, "lodash") {
				t.Errorf("expected jest and lodash missing, got %+v", res)
			}
		})

		t.Run(mode+": extra allowed suppresses only the allowed module", func(t *testing.T) {
			res := GetMissingNodeModulesFromTree(tree, nil, nil, map[string]bool{}, map[string]bool{"jest": true}, nearest)
			if hasModule(res, "jest") {
				t.Errorf("jest should be suppressed by extra allowed modules, got %+v", res)
			}
			if !hasModule(res, "lodash") {
				t.Errorf("lodash should still be reported missing, got %+v", res)
			}
		})
	}
}

func TestNodeModulesCmd_Resolution(t *testing.T) {
	// Mirrors the config-side behavior via the CLI strategy swap (CwdResolver vs SelfResolver).
	t.Run("missing: entry-package flags the followed package's own deps", func(t *testing.T) {
		out := runNodeModulesResolutionCmd(t, false, true, false /* entry */)
		if !strings.Contains(out, "lodash") {
			t.Errorf("entry-package missing should flag lodash, got:\n%s", out)
		}
		if !strings.Contains(out, "phantom-dep") {
			t.Errorf("entry-package missing should flag phantom-dep, got:\n%s", out)
		}
		if strings.Contains(out, "shared-only-util") {
			t.Errorf("entry-package missing should NOT flag shared-only-util (declared by the entry), got:\n%s", out)
		}
	})

	t.Run("missing: nearest-package validates against each file's own package", func(t *testing.T) {
		out := runNodeModulesResolutionCmd(t, false, true, true /* nearest */)
		if strings.Contains(out, "lodash") {
			t.Errorf("nearest-package missing should NOT flag lodash (declared by shared-lib), got:\n%s", out)
		}
		if !strings.Contains(out, "shared-only-util") {
			t.Errorf("nearest-package missing should flag shared-only-util (used by shared-lib, undeclared there), got:\n%s", out)
		}
		if !strings.Contains(out, "phantom-dep") {
			t.Errorf("nearest-package missing should flag phantom-dep, got:\n%s", out)
		}
	})

	t.Run("unused: nearest-package (variant A) flags entry deps unused by the entry's own files", func(t *testing.T) {
		entryOut := runNodeModulesResolutionCmd(t, true, false, false /* entry */)
		if strings.Contains(entryOut, "shared-only-util") {
			t.Errorf("entry-package unused should NOT flag shared-only-util (used somewhere in the tree), got:\n%s", entryOut)
		}

		nearestOut := runNodeModulesResolutionCmd(t, true, false, true /* nearest */)
		if !strings.Contains(nearestOut, "shared-only-util") {
			t.Errorf("nearest-package unused should flag shared-only-util (unused by the entry's own files), got:\n%s", nearestOut)
		}
	})
}
