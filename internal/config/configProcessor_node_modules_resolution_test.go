// loadAndProcessNodeModulesResolutionConfig writes a temp config into the fixture dir, processes it,
// and returns the single rule result.
func loadAndProcessNodeModulesResolutionConfig(t *testing.T, testCwd, cfg string) RuleResult {
	t.Helper()

	configPath := filepath.Join(testCwd, "node-modules-resolution-config.json")
	if err := os.WriteFile(configPath, []byte(cfg), 0644); err != nil {
		t.Fatalf("Failed to write config: %v", err)
	}
	t.Cleanup(func() { _ = os.Remove(configPath) })

	config, err := LoadConfig(configPath)
	if err != nil {
		t.Fatalf("Failed to load config: %v", err)
	}

	result, err := ProcessConfig(&config, testCwd, "package.json", "", false, false)
	if err != nil {
		t.Fatalf("ProcessConfig failed: %v", err)
	}
	if len(result.RuleResults) != 1 {
		t.Fatalf("expected exactly one rule result, got %d", len(result.RuleResults))
	}
	return result.RuleResults[0]
}

func sortedKeys(m map[string]bool) []string {
	out := make([]string, 0, len(m))
	for k := range m {
		out = append(out, k)
	}
	sort.Strings(out)
	return out
}

func missingModuleSet(rr RuleResult) map[string]bool {
	out := map[string]bool{}
	for _, m := range rr.MissingNodeModules {
		out[m.ModuleName] = true
	}
	return out
}

func unusedModuleSet(rr RuleResult) map[string]bool {
	out := map[string]bool{}
	for _, m := range rr.UnusedNodeModules {
		out[m.ModuleName] = true
	}
	return out
}

func unresolvedRequestSet(rr RuleResult) map[string]bool {
	out := map[string]bool{}
	for _, u := range rr.UnresolvedImports {
		out[u.Request] = true
	}
	return out
}

func assertSetEqual(t *testing.T, label string, got map[string]bool, want ...string) {
	t.Helper()
	wantSet := map[string]bool{}
	for _, w := range want {
		wantSet[w] = true
	}
	for _, w := range want {
		if !got[w] {
			t.Errorf("%s: expected %q to be present, got %v", label, w, sortedKeys(got))
		}
	}
	for g := range got {
		if !wantSet[g] {
			t.Errorf("%s: unexpected %q present, got %v", label, g, sortedKeys(got))
		}
	}
}

// nodeModulesResolutionRuleConfig builds a config that runs all three node-module checks for the
// main-app rule. resolutionLine is injected verbatim at the config root (empty to omit the field).
func nodeModulesResolutionRuleConfig(resolutionLine string) string {
	return `{
		"configVersion": "1.9",
		` + resolutionLine + `
		"rules": [
			{
				"path": "apps/main-app",
				"missingNodeModulesDetection": { "enabled": true },
				"unusedNodeModulesDetection": { "enabled": true },
				"unresolvedImportsDetection": { "enabled": true }
			}
		]
	}`
}

func TestConfigProcessor_NodeModulesResolution(t *testing.T) {
	testCwd, err := testutil.FixturePath("nodeModulesResolution")
	if err != nil {
		t.Fatalf("FixturePath: %v", err)
	}

	// In this fixture:
	//   apps/main-app declares: react, shared-only-util, shared-lib (workspace)
	//     - imports react (own dep) and shared-lib (workspace)
	//   packages/shared-lib declares: lodash
	//     - imports lodash (own dep), shared-only-util (declared by app, NOT shared), phantom-dep (nowhere)
	// main-app follows shared-lib, so shared-lib's imports enter the tree.

	t.Run("entry-package (default, field omitted) validates everything against the rule entry package.json", func(t *testing.T) {
		rr := loadAndProcessNodeModulesResolutionConfig(t, testCwd, nodeModulesResolutionRuleConfig(""))

		// lodash is declared by shared-lib but not by main-app -> false-positive missing under entry-package.
		// phantom-dep is declared nowhere -> genuinely missing.
		assertSetEqual(t, "missing/entry", missingModuleSet(rr), "lodash", "phantom-dep")

		// react (used by app), shared-only-util (used by shared-lib anywhere in the tree), and the
		// shared-lib workspace dep are all considered used -> nothing unused.
		assertSetEqual(t, "unused/entry", unusedModuleSet(rr))

		// Only phantom-dep is unresolved: shared-only-util resolves as a NodeModule of main-app and is
		// suppressed; lodash resolves as a NodeModule of shared-lib (so it is "missing" but not "unresolved").
		assertSetEqual(t, "unresolved/entry", unresolvedRequestSet(rr), "phantom-dep")
	})

	t.Run("nearest-package validates each import against the package.json owning its file", func(t *testing.T) {
		rr := loadAndProcessNodeModulesResolutionConfig(t, testCwd, nodeModulesResolutionRuleConfig(`"nodeModulesResolution": "nearest-package",`))

		// lodash is declared by shared-lib (its owner) -> no longer missing. shared-only-util is used by
		// shared-lib but NOT declared there -> phantom dep, correctly flagged. phantom-dep -> missing.
		assertSetEqual(t, "missing/nearest", missingModuleSet(rr), "shared-only-util", "phantom-dep")

		// variant A: shared-only-util is declared by main-app but never used by main-app's OWN files
		// (only shared-lib uses it) -> unused.
		assertSetEqual(t, "unused/nearest", unusedModuleSet(rr), "shared-only-util")

		// nearest-package suppresses nothing: every import not declared by its owning package is unresolved.
		assertSetEqual(t, "unresolved/nearest", unresolvedRequestSet(rr), "shared-only-util", "phantom-dep")
	})

	t.Run("explicit entry-package matches the default", func(t *testing.T) {
		rr := loadAndProcessNodeModulesResolutionConfig(t, testCwd, nodeModulesResolutionRuleConfig(`"nodeModulesResolution": "entry-package",`))
		assertSetEqual(t, "missing/explicit-entry", missingModuleSet(rr), "lodash", "phantom-dep")
		assertSetEqual(t, "unused/explicit-entry", unusedModuleSet(rr))
		assertSetEqual(t, "unresolved/explicit-entry", unresolvedRequestSet(rr), "phantom-dep")
	})
}

func TestParseConfig_NodeModulesResolution(t *testing.T) {
	base := func(field string) string {
		return `{"configVersion": "1.9", ` + field + `"rules": [{"path": "."}]}`
	}

	t.Run("omitted normalizes to entry-package", func(t *testing.T) {
		cfg, err := ParseConfig([]byte(base("")))
		if err != nil {
			t.Fatalf("ParseConfig: %v", err)
		}
		if cfg.NodeModulesResolution != NodeModulesResolutionEntryPackage {
			t.Errorf("expected default %q, got %q", NodeModulesResolutionEntryPackage, cfg.NodeModulesResolution)
		}
	})

	t.Run("nearest-package is accepted", func(t *testing.T) {
		cfg, err := ParseConfig([]byte(base(`"nodeModulesResolution": "nearest-package", `)))
		if err != nil {
			t.Fatalf("ParseConfig: %v", err)
		}
		if cfg.NodeModulesResolution != NodeModulesResolutionNearestPackage {
			t.Errorf("expected %q, got %q", NodeModulesResolutionNearestPackage, cfg.NodeModulesResolution)
		}
	})

	t.Run("invalid value is rejected", func(t *testing.T) {
		_, err := ParseConfig([]byte(base(`"nodeModulesResolution": "bogus", `)))
		if err == nil {
			t.Fatalf("expected error for invalid nodeModulesResolution value")
		}
		if !strings.Contains(err.Error(), "nodeModulesResolution") {
			t.Errorf("expected error to mention nodeModulesResolution, got: %v", err)
		}
	})
}
